package util

import (
	"crypto/rand"
	"math/big"
)

// Shuffle shuffles the elements of a slice.
func Shuffle[T any](s []T) []T {
	n := len(s)
	for i := n - 1; i > 0; i-- {
		jBig, err := rand.Int(rand.Reader, big.NewInt(int64(i+1)))
		if err != nil {
			panic(err)
		}

		j := int(jBig.Int64())
		s[i], s[j] = s[j], s[i]
	}
	return s
}
